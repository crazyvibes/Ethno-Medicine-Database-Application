<p>Ethnomedicine is a study or comparison of the traditional medicine practiced by various ethnic groups, and especially by indigenous peoples.
Ethnomedicine refers to a wide range of healthcare systems/structures, practices, beliefs, and therapeutic techniques that arise from indigenous cultural development.
Ethnomedicine is concerned with the cultural interpretations of health, disease and illness and also addresses the health care-seeking process and healing practices.
Ethnomedicine and the history of medicine have in common the description of medical systems,
whereby the separation is geographical in the former case and historical in the latter case.
</p>
