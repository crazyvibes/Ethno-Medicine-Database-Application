  <label for="subject">SIGNIFICANCE OF ETHNOMEDICINE</label>

  <p>Ethnomedicine is a specialist area that is not restricted to describing "exotic" healing practices, but it actually offers practical help in everyday clinical medicine. Thus, Ethnomedicine facilitates communication with patients from all walks of life. Moreover, an Ethnomedical Practitioner's observations and analysis within the scope of Ethnomedicine's understanding of the inherent conceptions held by their patients, with regard to illness and therapy, has heightened the medical and nursing fraternity.

Thus, these practitioners are better equipped to pinpoint and respond to their patient's needs. Ethnomedicine is considered to be an unbiased science by it's proponents.
  </p>
